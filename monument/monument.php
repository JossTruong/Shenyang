              <div class="d-block mb-4 h-100">
                <br>
                Le Palais Impérial de Shenyang (ou “Palais Moukden”), se situe au centre de Shenyang (chef-lieu de la province du Liaoning).<br><br>
                Il fut construit par l’Empereur Nurhaci en 1625 et étendu par l’Empereur Qianlong (1746-1748) en 1783.<br> 
                Il est le témoin de la fondation de la dernière dynastie (Qing), qui dirigea la Chine avant son expansion et son transfert vers Beijing, nouvelle capitale.<br>
                Il devint alors une annexe du nouveau palais impérial. <br>

                
                Différent de ce dernier, le Palais Impérial de Shenyang a été construit par les Empereurs Mandchous et possède la couleur (tuiles des toits vertes) ethnique de ses bâtisseurs.<br><br>

                Sa superficie est de 71 800 m² constituée de 114 édifices.
                <br>
                On distingue clairement trois sections, la section orientale, centrale et occidentale. 
                <br><div id="demo2" class="collapse"><br>
                La section orientale servait aux cérémonies importantes. Son élément central est le magnifique pavillon Da Zheng Dian où l’empereur gère les affaires généraux de l'empire. 
                <br>
                C’est un bâtiment octogonal qui a la forme d’une tente de tribu nomade.
                <br><br>

                La section centrale abritait le Palais Qingning, lieu d'habitat de l’empereur, de sa famille et ses concubines. Ainsi que le palais Chong Zheng Dian où l'empereur tenait sa cour.
                <br><br>

                La section occidentale comprend une partie nord qui comporte le Pavillon Wensu, une bibliothèque importante. Et une partie sud, le Hall Jiayin ainsi que la scène des représentations théâtrales, qui accueillait des petits banquets et des spectacles d’opéra.
                <br>
                </div>
<br>
                En 1926, le palais se transforme en musée et en 1949, il ouvrit ses portes au public, suite à la fondation de la République populaire de Chine. 
                <br><br>
                
                En 1961, le site fut inscrit sur la première liste de reliques nationales protégées par le Conseil d’État.                 
                <br><br>

                Les palais impériaux de Beijing et de Shenyang ont été inscrits sur la liste du patrimoine mondial (UNESCO), respectivement, en 1987 (inscription) et en 2004 (extension).<br>
              

