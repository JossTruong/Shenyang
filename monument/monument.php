                La Tombe Fuling, ou “Tombe de l’Est”, fût créé en 1629 pour l’Empereur Nurhaci (1559-1626), fondateur de la Dynastie Qing.<br><br> 
                Elle se situe au nord-est de Shenyang, à 10 km du centre ville. <br>
                Ce fût le site principal pour les cérémonies rituelles de la famille pendant toute la période Qing et sa structure compte 32 édifices. <br><br>
                Classement historique au patrimoine chinois: 1988<br>
                Classement au patrimoine mondial (UNESCO): 2004<br>
