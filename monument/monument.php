            <div class="col-lg-6 col-md-6 col-xs-6">
              <div class="d-block mb-4 h-100">
                La construction de ses tombes suivent les préceptes de la géomancie chinoise ( “ancêtre” du Feng Shui),  ainsi l’ensemble des structures repose sur un axe sud-nord jusqu’aux bâtiments funéraires.<br>
                Après le passage d’une première porte, débute une route centrale appelée “Voie Sacrée” ou “Voie des Esprits”. (566m pour Fuling, 1.2km pour Zhaoling)<br><br>

                Cette dernière se compose en 3 chemins:<br>
                1. Voie centrale: utilisée par les porteurs d’offrandes et les divinités.<br>
                2. Voie de gauche (Ouest): pour l’Empereur.<br>
                3. Voie de droite (Est): pour les fonctionnaires et agents impériaux. <br>

              <div id="demo" class="collapse"><br>
                Celle-ci est bordée symétriquement par des gardiens de pierre, sculptés dans un seul bloc. <br>
                Le symbolisme des animaux mythiques est fortement présent. Tel que le Qilin qui représente la justice, ou le lion, la puissance.<br>
                <br>
                En sa fin, portée sur son dos par une grande statue de tortue (emblème de longévité), une stèle raconte les exploits de l’Empereur. <br><br>

                En continuant vers une deuxième porte, une place fortifiée abrite cinq bâtiments. <br>Quatres à usage funéraire, le cinquième vise à faire des offrandes et recueillements aux morts enterrés dans le tumulus en son dos. <br>
