      <br><br>

      The central section housed the Qingning Palace, living place of the emperor, his family and his concubines.As well as the Chong Zheng Dian palace where the emperor held his court.
      <br><br>

      The western section includes a northern part that includes the Wensu Pavilion, an important library. And a southern part, the Jiayin Hall and the theatrical stage, which hosted small banquets and opera performances.
      <br>
      
      </div><br>
      In 1926, the palace became a museum and in 1949, it opened its doors to the public, following the founding of the People's Republic of China.
      <br><br>
      
      In 1961, the site was placed on the first list of national relics protected by the State Council.                 
      <br><br>

