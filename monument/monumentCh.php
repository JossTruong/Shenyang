      Shenyang Imperial Palace (ou “Mukden Palace”), It is located in center of Shenyang (provincial capital of Liaoning Province).<br><br>
      It was built by Emperor Nurhaci in 1625 and extended by Emperor Qianlong (1746-1748) in 1783.<br> 
      He is the witness of the foundation of the last dynasty of China ( Qing), which ruled China before its expansion and its relocation to Beijing, the new capital.<br>
      It becomes an annex of the new imperial palace. <br>

      
      Unlike the latter, the Shenyang Imperial Palace was built by the Manchu Emperors and has the color (green roof tiles) of its ethnic builders.<br><br>

      Its area is 71 800 m² consisting of 114 buildings.
      <br>
      We can clearly distinguish three sections, the eastern, central and western section.
      <br><div id="demo2" class="collapse"><br>
